    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String Customer = custID.getText();
            
            
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.cj.jdbc.driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/Airline_Project", "root" , "$@Ntosh0610");
            pre = con.prepareStatement("select * from customer where CustomerID= ?");
            pre.setString(1 , Customer);
            
            ResultSet rs = pre.executeQuery();
            
            if(rs.next() != false){
                firstName.setText(rs.getString("firstname"));
                lastName.setText(rs.getString("lastname"));
                passport.setText(rs.getString("passportno."));
                nationalID.setText(rs.getString("nationalID"));
                address.setText(rs.getString("address"));
                contact.setText(rs.getString("contact"));
                if(rs.getString("Gender").equals("male")){
                    male.setSelected(true);
                }else{
                    female.setSelected(true);
                }
                date.setText(rs.getString("date"));
            }else{
                JOptionPane.showMessageDialog(null,"Customer does not exist≈õ");
            }
           
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


